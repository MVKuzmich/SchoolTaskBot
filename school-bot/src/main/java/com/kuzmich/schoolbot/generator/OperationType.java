package com.kuzmich.schoolbot.generator;

/**
 * Тип операции для генератора заданий (1 класс).
 * Арифметика: сложение/вычитание. Числа: состав числа, сравнение, числовой ряд.
 * Каждое значение соответствует одному конкретному генератору.
 */
public enum OperationType {

    /** Сложение в пределах 10. */
    ADDITION_10,

    /** Вычитание в пределах 10. */
    SUBTRACTION_10,

    /** Сложение в пределах 20 без перехода через десяток. */
    ADDITION_20_NO_CARRY,

    /** Вычитание в пределах 20 без перехода через десяток. */
    SUBTRACTION_20_NO_CARRY,

    /** Состав числа 2–9 (например: 7 = __ + 3). */
    NUMBER_COMPOSITION_2_9,

    /** Состав числа 10 (только разложения десятки). */
    NUMBER_COMPOSITION_10,

    /** Состав числа 11–20 (например: 15 = 8 + __). */
    NUMBER_COMPOSITION_11_20,

    /** Состав числа смешанный 2–10 (все числа от 2 до 10). */
    NUMBER_COMPOSITION,

    /** Сравнение чисел (например: 5 __ 8, ответ &lt;). */
    COMPARISON,

    /** Продолжи числовой ряд (например: 3, 4, __, 6, 7). */
    NUMBER_SEQUENCE
}

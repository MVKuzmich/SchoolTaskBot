package com.kuzmich.schoolbot.state;

/**
 * Состояние пользователя в диалоге — «где мы в сценарии».
 * Используется для валидации ввода и перехода к следующему шагу.
 */
public enum UserState {

    /** Главное меню, ожидание выбора режима. */
    INITIAL,

    /** Ожидание согласия на обработку персональных данных (экран согласия показан). */
    AWAITING_CONSENT,

    /** Ожидание выбора режима (Генератор / Тренажёр). */
    AWAITING_MODE,

    /** Ожидание выбора класса (Начальная / Средняя школа). */
    AWAITING_SCHOOL_LEVEL,

    /** Ожидание выбора предмета (в MVP только математика). */
    AWAITING_SUBJECT,

    /** Ожидание выбора темы (арифметика, столбиком, умножение, деление и т.д.). */
    AWAITING_TOPIC,

    /** Ожидание выбора типа операции (сложение / вычитание / смешанные). */
    AWAITING_OPERATION_TYPE,

    /** Ожидание выбора количества (10, 20, 30 … или кастомный ввод). */
    AWAITING_QUANTITY,

    /** Ожидание текстового ввода числа (кастомное количество). */
    AWAITING_CUSTOM_QUANTITY,

    /** Показ summary, ожидание подтверждения «Создать». */
    AWAITING_CONFIRMATION,

    /** PDF генерируется. */
    GENERATING,

    /** Тренажёр: идёт сессия (post-MVP). */
    TRAINING_IN_PROGRESS,

    /** Тренажёр: ожидание ответа на пример (post-MVP). */
    AWAITING_ANSWER,

    /** Завершено (после выдачи PDF или завершения тренажёра). */
    COMPLETED,

    /** Ошибка. */
    ERROR
}
